    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tbltabl.getModel();
        model.setRowCount(0);
        for (NhanVien nv : nhanVienService.getList()) {
            Object[] row = {
                nv.getMa(),
                nv.getTen(),
                nv.getDiachi(),
                nv.getGioitinh()
            };
            model.addRow(row);
        }
    }

    private NhanVien getFormData() {
        String ma = txtMa.getText().trim();
        String ten = txtten.getText().trim();
        String diachi = txtDiaChi.getText().trim();
        String gioitinh = rdoNam.isSelected() == true ? "Nam" : "Nữ";

        if (ma.length() == 0 || ten.length() == 0 || diachi.length() == 0) {
            JOptionPane.showMessageDialog(this, "Ko được bỏ trống");
        }

        NhanVien nv = new NhanVien(ma, ten, diachi, gioitinh);
        return nv;
    }

    private boolean trungma(String txt) {
        for (NhanVien n : nhanVienService.getList()) {
            if (n.getMa().equalsIgnoreCase(txt)) {
                return true;
            }
        }
        return false;
    }

