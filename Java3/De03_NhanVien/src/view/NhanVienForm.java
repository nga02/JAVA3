    private void tbltablMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbltablMouseClicked
        int row = tbltabl.getSelectedRow();
        if (row == -1) {
            return;
        }
        String ma = tbltabl.getValueAt(row, 0).toString();
        String ten = tbltabl.getValueAt(row, 1).toString();
        String diachi = tbltabl.getValueAt(row, 2).toString();
        String gioitinh = tbltabl.getValueAt(row, 3).toString();

        txtMa.setText(ma);
        txtten.setText(ten);
        txtDiaChi.setText(diachi);
        if (gioitinh.equalsIgnoreCase("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }//GEN-LAST:event_tbltablMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NhanVien nv = getFormData();
        if (nv == null) {
            return;
        } else if (trungma(nv.getMa())) {
            return;
        }
        nhanVienService.insert(nv);
        loadTable();
        JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        String maNV = txtMa.getText();
        if (maNV.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống mã");
        }
        ArrayList<NhanVien> ds = nhanVienService.getList();
        for (int i = 0; i < ds.size(); i++) {
            NhanVien nv = ds.get(i);
            if (nv.getMa().equalsIgnoreCase(maNV)) {
                txtMa.setText(nv.getMa());
                txtten.setText(nv.getTen());
                txtDiaChi.setText(nv.getDiachi());
                if (nv.getGioitinh().equalsIgnoreCase("Nam")) {
                    rdoNam.setSelected(true);
                } else {
                    rdoNu.setSelected(true);
                }
                JOptionPane.showMessageDialog(this, "Đã tìm thấy nv có mã:" + maNV);
                loadTable();
                tbltabl.setRowSelectionInterval(i, i);
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "Không tìm thấy nv có mã:" + maNV);
    }//GEN-LAST:event_btnTimkiemActionPerformed

