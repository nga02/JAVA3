    private void tblSVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSVMouseClicked
        int row = tblSV.getSelectedRow();
        if (row == -1) {
            return;
        }
        SinhVien sv = qlsv.getByid(row);
        txtMaSV.setText(sv.getMaSV());
        txtHten.setText(sv.getHoTen());
        txtEmail.setText(sv.getEmail());
        txtSdt.setText(sv.getSoDT());
        if (sv.getGioiTinh() == 1) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        txtDChi.setText(sv.getdChi());

        if (sv.getHinhAnh().equalsIgnoreCase("No avata")) {
            lblAnh.setText("No avata");
            lblAnh.setIcon(null);
        } else {
            lblAnh.setText("");
            Image avartarImg = null;
            try {
                avartarImg = ImageIO.read(new File("D:\\Java3\\Ph26840_Assigment_Java3\\src\\images\\" + sv.getHinhAnh()));
            } catch (Exception e) {
                e.printStackTrace();
            }
            ImageIcon icon = new ImageIcon(avartarImg.getScaledInstance(128, 137, Image.SCALE_SMOOTH));
            lblAnh.setIcon(icon);
        }

    }//GEN-LAST:event_tblSVMouseClicked

