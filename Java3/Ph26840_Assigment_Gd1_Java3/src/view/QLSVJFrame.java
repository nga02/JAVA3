    private void loadTable() {
        List<SinhVien> ds = qlsv.getList();
        DefaultTableModel dtm = (DefaultTableModel) tblSV.getModel();
        dtm.setRowCount(0);
        for (SinhVien d : ds) {
            SinhVien sv = (SinhVien) d;
            dtm.addRow(new Object[]{
                sv.getMaSV(),
                sv.getHoTen(),
                sv.getEmail(),
                sv.getSoDT(),
                sv.getGioiTinh() == 1 ? "True" : "False",
                sv.getdChi(),
                sv.getHinhAnh()
            });
        }
    }

