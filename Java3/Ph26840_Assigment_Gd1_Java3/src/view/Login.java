    private boolean checkLogin(String userName, String pass) {
        List<User> ds = us.getList();
        for (User x : ds) {
            if (x.getUser().equalsIgnoreCase(userName) && x.getPass().equalsIgnoreCase(pass)) {
                return true;
            }
        }
        return false;
    }

