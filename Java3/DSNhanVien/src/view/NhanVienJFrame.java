    private boolean trungMa(String txt) {
        List<NhanVien> ds = nvSer.getList();
        for (NhanVien x : ds) {
            if (x.getManv().equalsIgnoreCase(txt)) {
                return true;
            }
        }
        return false;
    }

    private boolean checkSo(String txt) {
        Pattern p = Pattern.compile("\\d+");
        Matcher m = p.matcher(txt);
        return m.matches();
    }
