    private void tblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTableMouseClicked
        int row = tblTable.getSelectedRow();
        if (row == -1) {
            return;
        }
        String id = tblTable.getValueAt(row, 0).toString();
        String name = tblTable.getValueAt(row, 1).toString();
        String ma = tblTable.getValueAt(row, 2).toString();
        String gioiTinh = tblTable.getValueAt(row, 3).toString();
        String sdt = tblTable.getValueAt(row, 4).toString();
        String ngaySinh = tblTable.getValueAt(row, 5).toString();
        String chucvu = tblTable.getValueAt(row, 6).toString();

        lbId.setText(id);
        txtTen.setText(name);
        txtMa.setText(ma);
        txtNgaySinh.setText(ngaySinh);
        txtSdt.setText(sdt);
        cbbCv.setSelectedItem(chucvu);
        if (gioiTinh.equals("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }

    }//GEN-LAST:event_tblTableMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NhanVien nv = getFormData();
        if (nv == null) {
            return;
        } else if (trungMa(nv.getManv())) {
            JOptionPane.showMessageDialog(this, "Mã nhân viên không đc trùng");
            return;
        }
        nvSer.add(nv);
        loadTable();
        JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn cần chọn một dòng trên table");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Xác nhận xóa?");
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        String idStr = tblTable.getValueAt(row, 0).toString();
        nvSer.delete(idStr);
        loadTable();
        JOptionPane.showMessageDialog(this, "Xóa thành công!");
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tblTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hay chon mot dong tren table");
            return;
        }
//        int confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn chứ");
//        if (confirm == JOptionPane.NO_OPTION) {
//            return;
//        }
        NhanVien nv = getFormData();
        if (nv == null) {
            return;
        }
        String idStr = tblTable.getValueAt(row, 0).toString();
        nvSer.update(idStr, nv);
        loadTable();
        JOptionPane.showMessageDialog(this, "Update thành công!");

    }//GEN-LAST:event_btnSuaActionPerformed

