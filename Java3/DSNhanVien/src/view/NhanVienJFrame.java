    private void loadTable() {
        List<NhanVien> dsNv = nvSer.getList();
        DefaultTableModel dtm = (DefaultTableModel) tblTable.getModel();

        dtm.setRowCount(0);
        for (NhanVien nv : dsNv) {
            Object[] row = {
                nv.getId(),
                nv.getHoten(),
                nv.getManv(),
                nv.getGioiTinh(),
                nv.getSdt(),
                nv.getNgaySinh(),
                nv.getChucVu()
            };
            dtm.addRow(row);
        }
    }

