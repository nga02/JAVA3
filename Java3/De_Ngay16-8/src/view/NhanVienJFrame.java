    private void loadTable() {
        List<NhanVien> ds = nhanVienService.getList();
        DefaultTableModel model = (DefaultTableModel) tblTable.getModel();
        model.setRowCount(0);
        for (NhanVien d : ds) {
            Object[] row = {
                d.getMaNV(),
                d.getGioiTinh(),
                d.getDiaChi(),
                d.getMatKhau()
            };
            model.addRow(row);
        }
    }

    private NhanVien getFormData() {
        String ma = txtMaNV.getText().trim();
        String gioiTinh = txtGioiTinh.getText().trim();
        String diaChi = txtDiaChi.getText().trim();
        String matKhau = txtMatKhau.getText().trim();

        if (ma.length() == 0 || gioiTinh.length() == 0 || diaChi.length() == 0 || matKhau.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống");
            return null;
        } else if (!checkMatKhau(matKhau)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu phải là số");
            return null;
        } else if (matKhau.length() < 6 || matKhau.length() > 6) {
            JOptionPane.showMessageDialog(this, "Mật khẩu phải gồm 6 kí tự");
            return null;
        }
        NhanVien nv = new NhanVien(ma, gioiTinh, diaChi, matKhau);
        return nv;
    }

    private boolean checkMatKhau(String txt) {
        Pattern p = Pattern.compile("\\d+");
        Matcher m = p.matcher(txt);
        return m.matches();
    }

    private boolean trungMa(String txt) {
        List<NhanVien> lst = nhanVienService.getList();
        for (NhanVien nhanVien : lst) {
            if (nhanVien.getMaNV().equalsIgnoreCase(txt)) {
                return true;
            }
        }
        return false;
    }

