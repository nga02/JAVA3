    private void tblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTableMouseClicked
        int row = tblTable.getSelectedRow();
        if (row == -1) {
            return;
        }
        String ma = tblTable.getValueAt(row, 0).toString();
        String gioiTinh = tblTable.getValueAt(row, 1).toString();
        String diaChi = tblTable.getValueAt(row, 2).toString();
        String matKhau = tblTable.getValueAt(row, 3).toString();

        txtMaNV.setText(ma);
        txtGioiTinh.setText(gioiTinh);
        txtDiaChi.setText(diaChi);
        txtMatKhau.setText(matKhau);
    }//GEN-LAST:event_tblTableMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblTable.getSelectedRow();
        if (row == -1) {
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Xác nhận xoá");
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        String idS = tblTable.getValueAt(row, 0).toString();
        nhanVienService.delete(idS);
        loadTable();
        JOptionPane.showMessageDialog(this, "Xoá thành công");
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NhanVien nv = getFormData();
        if (nv == null) {
            return;
        } else if (trungMa(nv.getMaNV())) {
            JOptionPane.showMessageDialog(this, "Mã không được trùng");
            return;
        }
        nhanVienService.insert(nv);
        loadTable();
        JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String maNV = txtMaNV.getText().trim();

        List<NhanVien> ds = nhanVienService.getList();
        if (txtMaNV.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã nv k được để trống");
            return;
        } else {
            for (int i = 0; i < ds.size(); i++) {
                NhanVien d = ds.get(i);
                if (d.getMaNV().equalsIgnoreCase(maNV)) {
                    txtMaNV.setText(d.getMaNV());
                    txtGioiTinh.setText(d.getGioiTinh());
                    txtDiaChi.setText(d.getDiaChi());
                    txtMatKhau.setText(d.getMatKhau());

                    JOptionPane.showMessageDialog(this, "Đã tìm thấy nv có mã " + maNV);
                    loadTable();
                    tblTable.setRowSelectionInterval(i, i);
                    return;
                }
            }
        }
        JOptionPane.showMessageDialog(this, "Không tìm thấy");
    }//GEN-LAST:event_btnTimKiemActionPerformed

