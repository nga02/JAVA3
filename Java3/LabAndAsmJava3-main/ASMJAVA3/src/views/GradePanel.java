    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            if (checkVali()) {
                return;
            }
            Grade st = new Grade();
            st.setMasv(txtIDSV.getText());
            st.setTienganh(Float.parseFloat(txtTienganh.getText()));
            st.setTinhoc(Float.parseFloat(txtTinhoc.getText()));
            st.setGdtc(Float.parseFloat(txtGDTC.getText()));
            GradeDAO dao = new GradeDAO();
            if (ques("Bạn có muốn cập nhật điểm của sinh viên?")) {
                return;
            }
            if (dao.update(st)) {
                JOptionPane.showMessageDialog(null, "Điểm sinh viên đã được cập nhật!");
                fillToTable(true);
                btnNewActionPerformed(evt);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Lưu điểm sinh viên thất bại!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            if (MyValidation.isEmpty(txtIDSV, "Nhập id cần xóa")) {
                return;
            }
            GradeDAO dao = new GradeDAO();
            if (ques("Bạn có muốn xóa điểm của sinh viên?")) {
                return;
            }
            if (dao.delete(txtIDSV.getText())) {
                JOptionPane.showMessageDialog(null, "Điểm sinh viên đã được xóa!");
                fillToTable(true);
                btnNewActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(null, "Xóa điểm sinh viên thất bại!");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Xóa điểm sinh viên thất bại!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

