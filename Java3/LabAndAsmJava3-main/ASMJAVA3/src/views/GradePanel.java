    private void fillToTable(boolean xuat) {
        DefaultTableModel tblModel = (DefaultTableModel) tblTop3.getModel();
        tblModel.setRowCount(0);
        try {
            ArrayList<Grade> lst = GradeDAO.viewAll();
            if (xuat) {
                for (Grade st : lst) {
                    StudentDAO dao = new StudentDAO();
                    Student s = dao.findByID(st.getMasv());
                    String ten = s.getName();
                    Object[] addRow = {
                        st.getMasv(), ten, st.getTienganh(), st.getTinhoc(),
                        st.getGdtc(), st.getDTB()
                    };
                    tblModel.addRow(addRow);
                }
            } else {
                for (int i = 0; i < 3; i++) {
                    Grade st = lst.get(i);
                    StudentDAO dao = new StudentDAO();
                    Student s = dao.findByID(st.getMasv());
                    String ten = s.getName();
                    Object[] addRow = {
                        st.getMasv(), ten, st.getTienganh(), st.getTinhoc(),
                        st.getGdtc(), st.getDTB()
                    };
                    tblModel.addRow(addRow);
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private boolean checkVali() {
        if (MyValidation.isEmpty(txtIDSV, "Mời nhập mã sv")) {
            return true;
        }
        if (MyValidation.isEmpty(txtTienganh, "Mời nhập điểm tiếng anh")) {
            return true;
        }
        if (MyValidation.isNotNumber(txtTienganh, "Mời nhập điểm đúng định dạng")) {
            return true;
        }
        if (MyValidation.isEmpty(txtTinhoc, "Mời nhập điểm Tin học")) {
            return true;
        }
        if (MyValidation.isNotNumber(txtTinhoc, "Mời nhập điểm đúng định dạng")) {
            return true;
        }
        if (MyValidation.isEmpty(txtGDTC, "Mời nhập điểm GDTC")) {
            return true;
        }
        if (MyValidation.isNotNumber(txtGDTC, "Mời nhập điểm đúng định dạng")) {
            return true;
        }
        return false;
    }

    private boolean ques(String msg) {
        int chon = JOptionPane.showConfirmDialog(null, msg, "Question", JOptionPane.YES_NO_OPTION);
        if (chon == JOptionPane.YES_OPTION) {
            return false;
        }
        return true;
    }
