    private void tblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTableMouseClicked
        int row = tblTable.getSelectedRow();
        if (row == -1) {
            return;
        }
        String ma = tblTable.getValueAt(row, 0).toString();
        String ten = tblTable.getValueAt(row, 1).toString();
        String ngaySinh = tblTable.getValueAt(row, 2).toString();
        String gioiTinh = tblTable.getValueAt(row, 3).toString();
        String mqh = tblTable.getValueAt(row, 4).toString();
        
        lbMa.setText(ma);
        txtTen.setText(ten);
        txtNgaySInh.setText(ngaySinh);
        cbbMqh.setSelectedItem(mqh);
        if (rdoNam.equals("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }

    }//GEN-LAST:event_tblTableMouseClicked
    
    
   
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        Ban b = getFormData();
        if (b == null) {
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn chứ");
        if (confirm == JOptionPane.NO_OPTION) {
            return;
        }
        banService.add(b);
        loadTable();
        JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn cần chọn một dòng trên table");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Xác nhận xóa?");
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        
        String idStr = this.tblTable.getValueAt(row, 0).toString();
        // int id = Integer.parseInt(idStr);
        banService.delete(idStr);
        loadTable();
        JOptionPane.showMessageDialog(this, "Xoá thành công!");
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tblTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hay chon mot dong tren table");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn chứ");
        if (confirm == JOptionPane.NO_OPTION) {
            return;
        }
        Ban b = getFormData();
        if (b == null) {
            return;
        }
        String id = tblTable.getValueAt(row, 0).toString();
        banService.update(id, b);
        loadTable();
        JOptionPane.showMessageDialog(this, "Update thành công!");
    }//GEN-LAST:event_btnSuaActionPerformed

