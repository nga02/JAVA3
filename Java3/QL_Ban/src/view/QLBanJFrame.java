    private Ban getFormData() {
        
        String id = lbMa.getText().toString();
        String ten = txtTen.getText().trim();
        String ngaySinh = txtNgaySInh.getText();
        int gioiTinh = rdoNam.isSelected() == true ? 1 : 0;
        String mqh = cbbMqh.getSelectedItem().toString();
        Date date;
        if (ten.length() == 0 || ngaySinh.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống");
            return null;
        }
        SimpleDateFormat sdf = new SimpleDateFormat();
        sdf.applyPattern("yyyy-mm-dd");
        try {
            date = sdf.parse(ngaySinh);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sai định dạng ngày sinh");
            return null;
        }
        Ban b = new Ban(id, ten, mqh, date, gioiTinh);
        return b;
    }

