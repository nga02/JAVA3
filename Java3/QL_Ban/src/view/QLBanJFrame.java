    private void loadTable() {
        List<Ban> dsBan = banService.getList();
        DefaultTableModel model = (DefaultTableModel) tblTable.getModel();
        
        model.setRowCount(0);
        for (Ban b : dsBan) {
            Object[] rowData = {
                b.getMaBan(),
                b.getTenBan(),
                b.getNgaySinh(),
                b.getGioiTinh() == 1 ? "Nam" : "Nữ",
                b.getMoiQuanHe()
            
            };
            model.addRow(rowData);
        }
    }

